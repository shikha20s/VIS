package bean;

public class User {
	private String name;
	private int age;
	private int dob;
	private String add;
	private String email;
	private int connumber;
	private String cname;
	private String modalno;
	private String chasisno;
	private String engineno;
	private String enginecapcity;
	private String color;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDob() {
		return dob;
	}
	public void setDob(int dob) {
		this.dob = dob;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getConnumber() {
		return connumber;
	}
	public void setConnumber(int connumber) {
		this.connumber = connumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getModalno() {
		return modalno;
	}
	public void setModalno(String modalno) {
		this.modalno = modalno;
	}
	
	public String getChasisno() {
		return chasisno;
	}
	public void setChasisno(String chasisno) {
		this.chasisno = chasisno;
	}
	public String getEngineno() {
		return engineno;
	}
	public void setEngineno(String engineno) {
		this.engineno = engineno;
	}
	public String getEnginecap() {
		return enginecapcity;
	}
	public void setEnginecap(String enginecapcity) {
		this.enginecapcity = enginecapcity;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
}
